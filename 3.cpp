#include <iostream>

using namespace std;


int main()
{
	setlocale(0, "");

	int arr[10] = { 3, 4, 5, 6, 7, 8, 9, 1, 2, 2 };

	int result = 1;

	for (int i = 0; i < 10; i++)
	{
		result *= arr[i];

	}

	cout << result;
}

