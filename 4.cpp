#include <iostream>

using namespace std;


int main()
{
	setlocale(0, "");

	int num = 0;

	cout << "Ведите число: ";
	cin >> num;

	if (num > 0)
	{
		cout << "Число положительное";
	}
	else if (num < 0)
	{
		cout << "Число отрицательное";
	}
	else if (num == 0)
	{
		cout << "Число равняется 0";

	}
}

